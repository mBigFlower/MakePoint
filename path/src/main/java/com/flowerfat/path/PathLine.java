public class PathLine {
    public float beginX;
    public float beginY;
    public float endX;
    public float endY;

    public PathLine(float beginX, float beginY, float endX, float endY) {
        this.beginX = beginX;
        this.beginY = beginY;
        this.endX = endX;
        this.endY = endY;
    }
}
